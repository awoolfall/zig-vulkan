const w32 = @cImport({
    @cDefine("WIN32_LEAN_AND_MEAN", "1");
    @cInclude("windows.h");
    @cInclude("winuser.h");
});
const kc = @import("../input/keycode.zig");
const KeyCode = kc.KeyCode;

pub fn convert_windows_keycode(winkc: usize) ?KeyCode {
    return switch (winkc) {
        w32.VK_LBUTTON => KeyCode.MouseLeft,
        w32.VK_RBUTTON => KeyCode.MouseRight,
        0x04 => KeyCode.MouseMiddle,
        0x05 => KeyCode.MouseX1,
        0x06 => KeyCode.MouseX2,
        w32.VK_BACK => KeyCode.BackSpace,
        w32.VK_TAB => KeyCode.Tab,
        w32.VK_RETURN => KeyCode.Enter,
        w32.VK_SHIFT => KeyCode.Shift,
        w32.VK_CONTROL => KeyCode.Control,
        w32.VK_MENU => KeyCode.Alt,
        w32.VK_CAPITAL => KeyCode.CapsLock,
        w32.VK_ESCAPE => KeyCode.Escape,
        w32.VK_SPACE => KeyCode.Space,
        w32.VK_PRIOR => KeyCode.PageUp,
        w32.VK_NEXT => KeyCode.PageDown,
        w32.VK_END => KeyCode.End,
        w32.VK_HOME => KeyCode.Home,
        w32.VK_LEFT => KeyCode.ArrowLeft,
        w32.VK_RIGHT => KeyCode.ArrowRight,
        w32.VK_UP => KeyCode.ArrowUp,
        w32.VK_DOWN => KeyCode.ArrowDown,
        w32.VK_SNAPSHOT => KeyCode.PrintScreen,
        w32.VK_INSERT => KeyCode.Insert,
        w32.VK_DELETE => KeyCode.Delete,
        0x30 => KeyCode.Num0,
        0x31 => KeyCode.Num1,
        0x32 => KeyCode.Num2,
        0x33 => KeyCode.Num3,
        0x34 => KeyCode.Num4,
        0x35 => KeyCode.Num5,
        0x36 => KeyCode.Num6,
        0x37 => KeyCode.Num7,
        0x38 => KeyCode.Num8,
        0x39 => KeyCode.Num9,
        0x41 => KeyCode.A,
        0x42 => KeyCode.B,
        0x43 => KeyCode.C,
        0x44 => KeyCode.D,
        0x45 => KeyCode.E,
        0x46 => KeyCode.F,
        0x47 => KeyCode.G,
        0x48 => KeyCode.H,
        0x49 => KeyCode.I,
        0x4A => KeyCode.J,
        0x4B => KeyCode.K,
        0x4C => KeyCode.L,
        0x4D => KeyCode.M,
        0x4E => KeyCode.N,
        0x4F => KeyCode.O,
        0x50 => KeyCode.P,
        0x51 => KeyCode.Q,
        0x52 => KeyCode.R,
        0x53 => KeyCode.S,
        0x54 => KeyCode.T,
        0x55 => KeyCode.U,
        0x56 => KeyCode.V,
        0x57 => KeyCode.W,
        0x58 => KeyCode.X,
        0x59 => KeyCode.Y,
        0x5A => KeyCode.Z,
        w32.VK_LWIN => KeyCode.WindowsLeft,
        w32.VK_RWIN => KeyCode.WindowsRight,
        w32.VK_NUMPAD0 => KeyCode.Pad0,
        w32.VK_NUMPAD1 => KeyCode.Pad1,
        w32.VK_NUMPAD2 => KeyCode.Pad2,
        w32.VK_NUMPAD3 => KeyCode.Pad3,
        w32.VK_NUMPAD4 => KeyCode.Pad4,
        w32.VK_NUMPAD5 => KeyCode.Pad5,
        w32.VK_NUMPAD6 => KeyCode.Pad6,
        w32.VK_NUMPAD7 => KeyCode.Pad7,
        w32.VK_NUMPAD8 => KeyCode.Pad8,
        w32.VK_NUMPAD9 => KeyCode.Pad9,
        w32.VK_MULTIPLY => KeyCode.Multiply,
        w32.VK_ADD => KeyCode.Add,
        w32.VK_SEPARATOR => KeyCode.Seperator,
        w32.VK_SUBTRACT => KeyCode.Subtract,
        w32.VK_DECIMAL => KeyCode.Decimal,
        w32.VK_DIVIDE => KeyCode.Divide,
        w32.VK_F1 => KeyCode.F1,
        w32.VK_F2 => KeyCode.F2,
        w32.VK_F3 => KeyCode.F3,
        w32.VK_F4 => KeyCode.F4,
        w32.VK_F5 => KeyCode.F5,
        w32.VK_F6 => KeyCode.F6,
        w32.VK_F7 => KeyCode.F7,
        w32.VK_F8 => KeyCode.F8,
        w32.VK_F9 => KeyCode.F9,
        w32.VK_F10 => KeyCode.F10,
        w32.VK_F11 => KeyCode.F11,
        w32.VK_F12 => KeyCode.F12,
        0x7C => KeyCode.F13,
        0x7D => KeyCode.F14,
        0x7E => KeyCode.F15,
        0x7F => KeyCode.F16,
        0x80 => KeyCode.F17,
        0x81 => KeyCode.F18,
        0x82 => KeyCode.F19,
        0x83 => KeyCode.F20,
        0x84 => KeyCode.F21,
        0x85 => KeyCode.F22,
        0x86 => KeyCode.F23,
        0x87 => KeyCode.F24,
        w32.VK_NUMLOCK => KeyCode.NumLock,
        w32.VK_SCROLL => KeyCode.ScrollLock,
        w32.VK_LSHIFT => KeyCode.ShiftLeft,
        w32.VK_RSHIFT => KeyCode.ShiftRight,
        w32.VK_LCONTROL => KeyCode.ControlLeft,
        w32.VK_RCONTROL => KeyCode.ControlRight,
        w32.VK_LMENU => KeyCode.AltLeft,
        w32.VK_RMENU => KeyCode.AltRight,
        w32.VK_OEM_1 => KeyCode.SemiColon,
        w32.VK_OEM_PLUS => KeyCode.PlusEquals,
        w32.VK_OEM_COMMA => KeyCode.Comma,
        w32.VK_OEM_MINUS => KeyCode.Minus,
        w32.VK_OEM_PERIOD => KeyCode.Period,
        w32.VK_OEM_2 => KeyCode.SlashQuestionMark,
        w32.VK_OEM_3 => KeyCode.Tilde,
        w32.VK_OEM_4 => KeyCode.BraceLeft,
        w32.VK_OEM_5 => KeyCode.BackSlashPipe,
        w32.VK_OEM_6 => KeyCode.BraceRight,
        w32.VK_OEM_7 => KeyCode.QuoteApostrophe,
        else => null,
    };
}
